print("\n===== Desi Restaurant Management System =====")

for i in range(1, 6):

    print(f"\nCustomer {i}")

    item = input("Select item (karhai/biryani/bbq/chai): ").lower()

    print(menu(item))

    if item == "karhai":
        spice = input("Enter spice level (high/medium/low): ")
        print(karhai(spice))

    elif item == "bbq":
        pieces = int(input("Enter BBQ pieces: "))
        print(bbq(pieces))

    elif item == "chai":
        sugar = int(input("Enter sugar level: "))
        print(chai(sugar))

